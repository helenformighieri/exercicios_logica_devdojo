package variaveis_operadores_tiposDeDados;

public class ExercicioQuatorze {
    public static void main(String[] args) {

        int num1 = 32;
        int num2 = 12;

        System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
        System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
        System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
        System.out.println(num1 + " x " + num2 + " = " + (num1 * num2));
    }
}
